public class Intervalo {

		private int ts, tf;

		public int getTs() {
			return ts;
		}

		public void setTs(int ts) {
			this.ts = ts;
		}

		public int getTf() {
			return tf;
		}

		public void setTf(int tf) {
			this.tf = tf;
		}

		@Override
		public String toString() {

			return "Intervalo [ts=" + ts + ", tf=" + tf + "] \n";
		}
	}